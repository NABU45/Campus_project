    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String studentID = jTextField8.getText();
        String name = jTextField9.getText();
        String fatherName = jTextField10.getText();
        String courseName = (String) jComboBox1.getSelectedItem();
        String branchName = (String) jComboBox2.getSelectedItem();
        
    
        
// Check for empty fieldsw
        if (studentID.isEmpty() || name.isEmpty() || fatherName.isEmpty() || courseName.isEmpty() || branchName == null || branchName.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please fill in all fields");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                st.executeUpdate("INSERT INTO student VALUES('" + studentID + "', '" + name + "','" + fatherName + "','" + courseName + "', '" + branchName + "' )");
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                setVisible(false);
                new newStudent().setVisible(true);
            } catch (SQLIntegrityConstraintViolationException e) {
                JOptionPane.showMessageDialog(null, "Student ID already exists");
                setVisible(false);
                new newStudent().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Unexpected error");
                e.printStackTrace(); // Log the exception for debugging purposes
                setVisible(false);
                new newStudent().setVisible(true);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

